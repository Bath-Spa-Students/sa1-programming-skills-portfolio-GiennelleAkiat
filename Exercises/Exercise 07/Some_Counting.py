#===============================================================
#------------------------0 TO 50
#===============================================================

for i in range(51):
    print(i)

#===============================================================
#------------------------50 to 0
#===============================================================

for i in reversed(range(51)):
    print(i)

#===============================================================
#------------------------30 to 50
#===============================================================

for i in range (30, 51):
    print(i)

#===============================================================
#------------------------50 to 10 in 2
#===============================================================

for i in range(50, 10-2, -2):
    print(i)

